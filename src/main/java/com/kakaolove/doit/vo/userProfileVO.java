package com.kakaolove.doit.vo;

public class userProfileVO {
	private String id; // 아이디
	private String indroduce; // 자기소개
	private String filed; // 전문분야
	private String spec; //경력사항
	private String edumajor;// 학력 및 전공
	private String certificate; // 보유자격증
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIndroduce() {
		return indroduce;
	}
	public void setIndroduce(String indroduce) {
		this.indroduce = indroduce;
	}
	public String getFiled() {
		return filed;
	}
	public void setFiled(String filed) {
		this.filed = filed;
	}
	public String getSpec() {
		return spec;
	}
	public void setSpec(String spec) {
		this.spec = spec;
	}
	public String getEdumajor() {
		return edumajor;
	}
	public void setEdumajor(String edumajor) {
		this.edumajor = edumajor;
	}
	public String getCertificate() {
		return certificate;
	}
	public void setCertificate(String certificate) {
		this.certificate = certificate;
	}
	
	
}
